    public void setBookjComboBox() {
        convertArray(library, readers);
        jComboBoxBooks.setModel(new javax.swing.DefaultComboBoxModel<>(books));
        jComboBoxReaders.setModel(new javax.swing.DefaultComboBoxModel<>(readerName));

        jComboBoxBooks.addActionListener(controller);
        jComboBoxBooks.setActionCommand("book");

        jComboBoxReaders.addActionListener(controller);
        jComboBoxReaders.setActionCommand("reader");
    }

    private void convertArray(ArrayList<Book> booksLibrary, ArrayList<Reader> readers) {
        books = new String[booksLibrary.size()];
        for (int i = 0; i < booksLibrary.size(); i++) {
            books[i] = booksLibrary.get(i).getBookTitle();
        }
        readerName = new String[readers.size()];
        for (int i = 0; i < readers.size(); i++) {
            readerName[i] = readers.get(i).getIdReader()+" | "+readers.get(i).getReaderName() + " " + readers.get(i).getReaderSurname();
        }
        Arrays.sort(books);
        Arrays.sort(readerName);
    }

